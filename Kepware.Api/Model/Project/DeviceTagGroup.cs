using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Text.Json.Serialization;
using System.Threading.Tasks;
using YamlDotNet.Serialization;

namespace Kepware.Api.Model
{
    /// <summary>
    /// Represents a tag group in a device
    /// </summary>
    [RecursiveEndpoint("/config/v1/project/channels/{channelName}/devices/{deviceName}", "/tag_groups/{groupName}", typeof(DeviceTagGroup), suffix: "/tag_groups/{groupName}")]
    public class DeviceTagGroup : NamedEntity
    {
        public DeviceTagGroup()
        {

        }

        public DeviceTagGroup(string name, Device owner)
        {
            Owner = owner;
            Name = name;
        }

        public DeviceTagGroup(string name, DeviceTagGroup owner)
        {
            Owner = owner;
            Name = name;
        }

        /// <summary>
        /// Gets or sets the tags in the tag group
        /// </summary>
        [YamlIgnore]
        [JsonPropertyName("tags")]
        [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
        public DeviceTagCollection? Tags { get; set; }

        /// <summary>
        /// Recursively cleans up the tag group and all its children
        /// </summary>
        [YamlIgnore]
        [JsonPropertyName("tag_groups")]
        [JsonIgnore(Condition = JsonIgnoreCondition.WhenWritingNull)]
        public DeviceTagGroupCollection? TagGroups { get; set; }

        /// <summary>
        /// Get a flag indicating if the tag group was autogenerated
        /// </summary>
        [YamlIgnore]
        [JsonIgnore]
        public bool IsAutogenerated => GetDynamicProperty<bool>(Properties.NonSerialized.TagGroupAutogenerated);

        /// <summary>
        /// Gets the number of tags that are defined locally within this tag group.
        /// </summary>
        /// <remarks>Use this property to determine how many tags are associated with the current
        /// instance, excluding tags in children tag groups. NOTE: Provides the count from the Kepware instance 
        /// and only updates when loading the tag group configuration.</remarks>
        [YamlIgnore]
        [JsonIgnore]
        public int LocalTagCount => GetDynamicProperty<int>(Properties.DeviceTagGroup.LocalTagCount);

        /// <summary>
        /// Gets the total number of tags included in the tag group and all children tag groups.
        /// </summary>
        /// <remarks>Use this property to determine how many tags are associated with the current
        /// instance, including tags in children tag groups. NOTE: Provides the count from the Kepware instance 
        /// and only updates when loading the tag group configuration.</remarks>
        [YamlIgnore]
        [JsonIgnore]
        public int TotalTagCount => GetDynamicProperty<int>(Properties.DeviceTagGroup.TotalTagCount);

        /// <summary>
        /// Recursively cleans up the tag group and all its children
        /// </summary>
        /// <param name="defaultValueProvider"></param>
        /// <param name="blnRemoveProjectId"></param>
        /// <param name="cancellationToken"></param>
        /// <returns></returns>
        public override async Task Cleanup(IKepwareDefaultValueProvider defaultValueProvider, bool blnRemoveProjectId = false, CancellationToken cancellationToken = default)
        {
            await base.Cleanup(defaultValueProvider, blnRemoveProjectId, cancellationToken).ConfigureAwait(false);

            if (Tags != null)
            {
                foreach (var tag in Tags)
                {
                    await tag.Cleanup(defaultValueProvider, blnRemoveProjectId, cancellationToken).ConfigureAwait(false);
                }
            }

            if (TagGroups != null)
            {
                foreach (var tagGroup in TagGroups)
                {
                    await tagGroup.Cleanup(defaultValueProvider, blnRemoveProjectId, cancellationToken).ConfigureAwait(false);
                }
            }
        }
    }
}
